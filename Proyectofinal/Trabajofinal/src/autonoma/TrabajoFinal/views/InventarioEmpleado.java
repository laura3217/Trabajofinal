    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = this.tablaProductos.getSelectedRow();
        if(fila>=0){
            Producto p = this.productos.get(fila);
            ActualizarPaciente ventanaActualizar = new ActualizarPaciente(this.ventanaPricipal, true, tienda, this, p);
            ventanaActualizar.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Por favor seleccione el producto que desea eliminar");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

